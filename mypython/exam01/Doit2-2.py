#산술연산
print( 10 + 2 )  
print( 10 - 2 ) 
print( 10 * 2 ) 
print( 10 / 2 ) 
print( 10 ** 2 ) 
print( 10 // 2 )    #몫을 정수로 리턴, 가장 가까운 정수로 내림 (음의 무한)
print( 10 % 2 )     #나머지를 리턴

print('//연산자 번외편')
print( 10 // 3 )
print( 7 // 2 ) #3.5 -> 소수점 이하는 그냥 내린다.
print( -7 // 2 ) #-3.5 -> 음의 무한으로 내려버리니까 -4
                 # 연산자는 음의 무한대로 가장 가까운 정수로 내림한다.